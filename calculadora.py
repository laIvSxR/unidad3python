#sumar
def suma(numero_1:int,numero_2) :
    return numero_2 + numero_1
#resta
def resta(numero_1:int,numero_2) :
    return numero_1 - numero_2
#division
def division(numero_1:int,numero_2) :
    return numero_1 / numero_2
#multipicacion
def multiplicacion(numero_1:int,numero_2) :
    return numero_1 * numero_2

#exponecial
def elevar(numero_1:int,numero_2) :
    return numero_1**numero_2